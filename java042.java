//EVEN ODD
public class java042 {
    public static void main(String[] args) {
        System.out.println("The odd numbers are:");

        for (int i = 1; i <= 10; i += 2) {
            System.out.print(i + " ");
        }

        System.out.println("\nThe even numbers are:");

        for (int j = 2; j <= 10; j += 2) {
            System.out.print(j + " ");
        }
    }
}
